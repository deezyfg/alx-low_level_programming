#include <stdlib.h>
#include <stdio.h>

/**
* add - Add two integers.
* @a: First integer.
* @b: Second integer.
* Return: Result of the addition.
*/
int add(int a, int b)
{
	return (a + b);
}

/**
* sub - Subtract two integers.
* @a: First integer.
* @b: Second integer.
* Return: Result of the subtraction.
*/
int sub(int a, int b)
{
	return (a - b);
}

/**
* mod - Calculate the remainder
*	of division of two integers.
* @a: First integer.
* @b: Second integer.
* Return: Remainder of the division (a % b).
*/
int mod(int a, int b)
{
	return (a % b);
}

/**
* mul - Multiply two integers.
* @a: First integer.
* @b: Second integer.
* Return: Product of the multiplication.
*/
int mul(int a, int b)
{
	return (a * b);
}

/**
* div1 - Divide two integers.
* @a: First integer.
* @b: Second integer.
* Return: Result of the division (a / b).
*/
int div1(int a, int b)
{
	return (a / b);
}
